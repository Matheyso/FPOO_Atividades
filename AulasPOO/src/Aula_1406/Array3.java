package Aula_1406;

public class Array3 {

    public static void main(String[] args) {
        String[] nomes = new String[3];
        nomes[0] = "Kelvin";
        nomes[1] = "Carla";
        nomes[2] = "Kaique";
        for (String zoi : nomes) {
            System.out.println("nome: " + zoi);
        }
    }
}
